
def sort_list(lst):
    """
    Sorts a list of numbers in ascending order.

    Args:
        lst (list): The list of numbers to sort.

    Returns:
        list: The sorted list.
    """
    return sorted(lst)

# Example usage:
input_list = [5, 2, 9, 1, 5, 6]
print("Input:", input_list)
print("Output:", sort_list(input_list))
